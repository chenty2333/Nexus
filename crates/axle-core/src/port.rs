//! Port queue semantics used by user packets and reactor-delivered signal packets.
//!
//! This is a host-testable semantic model with explicit overflow rules:
//! - A Port has a fixed `capacity` in packets.
//! - `kernel_reserve` enforces that user packets cannot consume the last N slots,
//!   preventing starvation of kernel-generated packets.
//! - Kernel-generated packets are admitted up to total capacity.
//!
//! Async wait observer state lives in `observer.rs`; `PortState` is only the packet
//! queue and readiness surface.

use alloc::collections::VecDeque;
use alloc::vec;
use alloc::vec::Vec;

use crate::capability::ObjectKey;
use crate::revocation::RevocationSet;
use crate::signals::Signals;
use crate::timer::Time;

/// Identifier for one waitable object incarnation.
///
/// In kernel this corresponds to `(object_id, generation)`. Host tests may keep the generation at
/// zero when they do not model slot reuse.
pub type WaitableId = ObjectKey;

/// Key associated with a port packet (Zircon's `key` field).
pub type PortKey = u64;

/// Port packet kind.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum PacketKind {
    /// A user packet (from `port_queue`).
    User,
    /// A signal packet generated by `wait_async`.
    Signal,
}

/// A simplified port packet.
///
/// This mirrors the most important observable fields of Zircon's packet types.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub struct Packet {
    /// User-provided key.
    pub key: PortKey,
    /// Packet kind.
    pub kind: PacketKind,
    /// For signal packets: the object being observed.
    pub waitable: WaitableId,
    /// For signal packets: watched signal mask that triggered this observer.
    pub trigger: Signals,
    /// For signal packets: observed signals snapshot (often OR-merged on overflow).
    pub observed: Signals,
    /// For signal packets: merged trigger count (>= 1 for signal packets, 0 for user packets).
    pub count: u32,
    /// For signal packets: trigger timestamp; 0 when timestamp delivery was not requested.
    pub timestamp: Time,
    /// Kernel-internal revocation provenance for deferred control-plane packets.
    pub revocation: RevocationSet,
    /// For user packets: caller-provided status.
    pub status: i32,
    /// For user packets: raw 32-byte payload.
    pub user: [u64; 4],
}

impl Packet {
    /// Create a user packet.
    pub fn user(key: PortKey) -> Self {
        Self::user_with_data(key, 0, [0; 4])
    }

    /// Create a user packet with explicit status and payload.
    pub fn user_with_data(key: PortKey, status: i32, user: [u64; 4]) -> Self {
        Self {
            key,
            kind: PacketKind::User,
            waitable: ObjectKey::INVALID,
            trigger: Signals::NONE,
            observed: Signals::NONE,
            count: 0,
            timestamp: 0,
            revocation: RevocationSet::none(),
            status,
            user,
        }
    }

    /// Create a signal packet.
    pub fn signal(
        key: PortKey,
        waitable: WaitableId,
        trigger: Signals,
        observed: Signals,
        count: u32,
        timestamp: Time,
    ) -> Self {
        Self::signal_with_revocation(
            key,
            waitable,
            trigger,
            observed,
            count,
            timestamp,
            RevocationSet::none(),
        )
    }

    /// Create a signal packet with explicit deferred revocation provenance.
    pub fn signal_with_revocation(
        key: PortKey,
        waitable: WaitableId,
        trigger: Signals,
        observed: Signals,
        count: u32,
        timestamp: Time,
        revocation: RevocationSet,
    ) -> Self {
        Self {
            key,
            kind: PacketKind::Signal,
            waitable,
            trigger,
            observed,
            count,
            timestamp,
            revocation,
            status: 0,
            user: [0; 4],
        }
    }
}

/// Timestamp mode requested for async wait packets.
#[derive(Clone, Copy, Debug, Default, PartialEq, Eq)]
pub enum WaitAsyncTimestamp {
    /// Do not include a timestamp in delivered packets.
    #[default]
    None,
    /// Use the kernel monotonic timeline.
    Monotonic,
    /// Use the kernel boot timeline.
    ///
    /// In current bring-up Axle has no suspend/resume distinction yet, so boot
    /// and monotonic time are sourced from the same monotonic clock.
    Boot,
}

/// Options for an async wait.
#[derive(Clone, Copy, Debug, Default, PartialEq, Eq)]
pub struct WaitAsyncOptions {
    /// Edge-triggered: fire only on a transition from not-satisfied -> satisfied.
    ///
    /// For level-triggered waits, packets can be delivered immediately if already satisfied.
    pub edge_triggered: bool,
    /// Which timestamp mode to include in signal packets, if any.
    pub timestamp: WaitAsyncTimestamp,
}

/// Errors used by port queue and observer operations.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum PortError {
    /// Queue is full for this operation and the caller should wait and retry.
    ShouldWait,
    /// Async wait already exists for a given registration key.
    AlreadyExists,
    /// Cancel requested for a non-existent wait.
    NotFound,
}

/// Queue-backend failure returned by one concrete packet-storage implementation.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum PacketQueueError {
    /// The backend cannot currently accept another packet.
    Full,
    /// The backend failed to persist one packet even though a slot was available.
    Backend,
}

/// Stable telemetry snapshot for one port queue.
#[derive(Clone, Copy, Debug, Default, PartialEq, Eq)]
pub struct PortTelemetrySnapshot {
    /// Total queue capacity.
    pub capacity: u32,
    /// Slots reserved for kernel packets.
    pub kernel_reserve: u32,
    /// Current queued-packet depth.
    pub current_depth: u32,
    /// Peak queued-packet depth observed so far.
    pub peak_depth: u32,
    /// Successful user-packet enqueues.
    pub user_queue_count: u64,
    /// User enqueue attempts rejected with `ShouldWait`.
    pub user_should_wait_count: u64,
    /// User rejections caused by the kernel-reserve ceiling.
    pub user_reserve_hit_count: u64,
    /// User rejections caused by total queue fullness.
    pub user_full_hit_count: u64,
    /// Successful kernel-packet enqueues.
    pub kernel_queue_count: u64,
    /// Kernel enqueue attempts rejected with `ShouldWait`.
    pub kernel_should_wait_count: u64,
    /// Successful packet dequeues.
    pub pop_count: u64,
    /// Number of recorded depth samples.
    pub depth_sample_count: u64,
    /// Median observed depth.
    pub depth_p50: u32,
    /// 90th-percentile observed depth.
    pub depth_p90: u32,
    /// 99th-percentile observed depth.
    pub depth_p99: u32,
}

#[derive(Clone, Debug)]
struct PortTelemetryState {
    current_depth: u32,
    peak_depth: u32,
    user_queue_count: u64,
    user_should_wait_count: u64,
    user_reserve_hit_count: u64,
    user_full_hit_count: u64,
    kernel_queue_count: u64,
    kernel_should_wait_count: u64,
    pop_count: u64,
    depth_sample_count: u64,
    depth_hist: Vec<u64>,
}

impl PortTelemetryState {
    fn new(capacity: usize) -> Self {
        let depth_hist = vec![0; capacity.saturating_add(1)];
        let mut state = Self {
            current_depth: 0,
            peak_depth: 0,
            user_queue_count: 0,
            user_should_wait_count: 0,
            user_reserve_hit_count: 0,
            user_full_hit_count: 0,
            kernel_queue_count: 0,
            kernel_should_wait_count: 0,
            pop_count: 0,
            depth_sample_count: 0,
            depth_hist,
        };
        state.record_depth(0);
        state
    }

    fn record_depth(&mut self, depth: usize) {
        let index = depth.min(self.depth_hist.len().saturating_sub(1));
        self.current_depth = index as u32;
        self.peak_depth = self.peak_depth.max(self.current_depth);
        self.depth_sample_count = self.depth_sample_count.saturating_add(1);
        if let Some(sample) = self.depth_hist.get_mut(index) {
            *sample = sample.saturating_add(1);
        }
    }

    fn percentile_depth(&self, numer: u64, denom: u64) -> u32 {
        if self.depth_sample_count == 0 || self.depth_hist.is_empty() {
            return 0;
        }
        let threshold = self
            .depth_sample_count
            .saturating_mul(numer)
            .saturating_add(denom.saturating_sub(1))
            / denom;
        let mut cumulative = 0_u64;
        for (depth, &count) in self.depth_hist.iter().enumerate() {
            cumulative = cumulative.saturating_add(count);
            if cumulative >= threshold.max(1) {
                return depth as u32;
            }
        }
        self.depth_hist.len().saturating_sub(1) as u32
    }

    fn snapshot(&self, capacity: usize, kernel_reserve: usize) -> PortTelemetrySnapshot {
        PortTelemetrySnapshot {
            capacity: capacity as u32,
            kernel_reserve: kernel_reserve as u32,
            current_depth: self.current_depth,
            peak_depth: self.peak_depth,
            user_queue_count: self.user_queue_count,
            user_should_wait_count: self.user_should_wait_count,
            user_reserve_hit_count: self.user_reserve_hit_count,
            user_full_hit_count: self.user_full_hit_count,
            kernel_queue_count: self.kernel_queue_count,
            kernel_should_wait_count: self.kernel_should_wait_count,
            pop_count: self.pop_count,
            depth_sample_count: self.depth_sample_count,
            depth_p50: self.percentile_depth(50, 100),
            depth_p90: self.percentile_depth(90, 100),
            depth_p99: self.percentile_depth(99, 100),
        }
    }
}

/// Packet queue backend used by the port state machine.
///
/// Implementations are not required to enforce a capacity limit themselves.
/// Capacity control is the responsibility of [`PortState`], which gates
/// admission through user-quota and kernel-reserve checks before calling
/// [`push_back`](PacketQueue::push_back).  A backend that always succeeds
/// (like [`VecPortQueue`]) is correct as long as `PortState` is the sole
/// entry point for enqueue operations.
pub trait PacketQueue: core::fmt::Debug {
    /// Number of queued packets currently stored.
    fn len(&self) -> usize;

    /// Returns `true` if no packets are queued.
    fn is_empty(&self) -> bool {
        self.len() == 0
    }

    /// Push one packet to the back of the queue.
    ///
    /// Returns a backend-specific storage failure when the packet could not be stored.
    fn push_back(&mut self, pkt: Packet) -> Result<(), PacketQueueError>;

    /// Pop one packet from the front of the queue.
    fn pop_front(&mut self) -> Option<Packet>;

    /// Retain only packets that satisfy `keep`.
    fn retain<F>(&mut self, keep: F)
    where
        F: FnMut(&Packet) -> bool;
}

/// Default in-memory packet queue used by host tests and simple kernel bring-up.
#[derive(Debug, Default)]
pub struct VecPortQueue {
    packets: VecDeque<Packet>,
}

impl VecPortQueue {
    /// Create an empty queue with room for at least `capacity` packets.
    pub fn with_capacity(capacity: usize) -> Self {
        Self {
            packets: VecDeque::with_capacity(capacity),
        }
    }
}

impl PacketQueue for VecPortQueue {
    fn len(&self) -> usize {
        self.packets.len()
    }

    fn push_back(&mut self, pkt: Packet) -> Result<(), PacketQueueError> {
        self.packets.push_back(pkt);
        Ok(())
    }

    fn pop_front(&mut self) -> Option<Packet> {
        self.packets.pop_front()
    }

    fn retain<F>(&mut self, mut keep: F)
    where
        F: FnMut(&Packet) -> bool,
    {
        self.packets.retain(|packet| keep(packet));
    }
}

#[derive(Clone, Copy, Debug)]
struct KernelReserveTicket;

/// A Zircon-like port queue with reservation semantics.
#[derive(Debug)]
pub struct PortState<Q: PacketQueue> {
    capacity: usize,
    kernel_reserve: usize,
    q: Q,
    user_in_q: usize,
    telemetry: PortTelemetryState,
}

/// Default port semantic state backed by an in-memory queue.
pub type Port = PortState<VecPortQueue>;

impl Port {
    /// Create a new port backed by the default in-memory queue.
    ///
    /// - `capacity` is in packets.
    /// - `kernel_reserve` reserves that many packet slots for kernel-generated packets.
    pub fn new(capacity: usize, kernel_reserve: usize) -> Self {
        Self::with_queue(
            capacity,
            kernel_reserve,
            VecPortQueue::with_capacity(capacity),
        )
    }
}

impl<Q: PacketQueue> PortState<Q> {
    /// Create a new port backed by `queue`.
    ///
    /// - `capacity` is in packets.
    /// - `kernel_reserve` reserves that many packet slots for kernel-generated packets.
    pub fn with_queue(capacity: usize, kernel_reserve: usize, queue: Q) -> Self {
        assert!(capacity > 0, "capacity must be non-zero");
        assert!(
            kernel_reserve <= capacity,
            "kernel_reserve must be <= capacity"
        );
        Self {
            capacity,
            kernel_reserve,
            q: queue,
            user_in_q: 0,
            telemetry: PortTelemetryState::new(capacity),
        }
    }

    /// Consume the port and return its packet queue backend.
    pub fn into_queue(self) -> Q {
        self.q
    }

    /// Total packets currently queued.
    pub fn len(&self) -> usize {
        self.q.len()
    }

    /// Returns `true` if there are no queued packets.
    pub fn is_empty(&self) -> bool {
        self.q.is_empty()
    }

    /// Current signal state for this port object.
    ///
    /// We follow Zircon's convention where ports become `READABLE` when at least
    /// one packet is queued. `WRITABLE` reflects whether a user packet could be
    /// queued without hitting backpressure.
    pub fn signals(&self) -> Signals {
        let mut s = Signals::NONE;
        if !self.q.is_empty() {
            s = s | Signals::OBJECT_READABLE;
        }
        if self.user_in_q < self.user_quota() && self.q.len() < self.capacity {
            s = s | Signals::OBJECT_WRITABLE;
        }
        s
    }

    /// Queue capacity.
    pub const fn capacity(&self) -> usize {
        self.capacity
    }

    /// Reserved slots for kernel packets.
    pub const fn kernel_reserve(&self) -> usize {
        self.kernel_reserve
    }

    /// Stable telemetry snapshot for this port.
    pub fn telemetry_snapshot(&self) -> PortTelemetrySnapshot {
        self.telemetry.snapshot(self.capacity, self.kernel_reserve)
    }

    fn user_quota(&self) -> usize {
        self.capacity - self.kernel_reserve
    }

    fn reserve_kernel_slot(&self) -> Option<KernelReserveTicket> {
        (self.q.len() < self.capacity).then_some(KernelReserveTicket)
    }

    /// Queue a user packet.
    ///
    /// Returns `ShouldWait` if the user quota is exhausted or the queue is full.
    pub fn queue_user(&mut self, pkt: Packet) -> Result<(), PortError> {
        debug_assert_eq!(pkt.kind, PacketKind::User);
        if self.user_in_q >= self.user_quota() {
            self.telemetry.user_should_wait_count =
                self.telemetry.user_should_wait_count.saturating_add(1);
            self.telemetry.user_reserve_hit_count =
                self.telemetry.user_reserve_hit_count.saturating_add(1);
            return Err(PortError::ShouldWait);
        }
        if self.q.len() >= self.capacity {
            self.telemetry.user_should_wait_count =
                self.telemetry.user_should_wait_count.saturating_add(1);
            self.telemetry.user_full_hit_count =
                self.telemetry.user_full_hit_count.saturating_add(1);
            return Err(PortError::ShouldWait);
        }
        if self.q.push_back(pkt).is_err() {
            return Err(PortError::ShouldWait);
        }
        self.user_in_q += 1;
        self.telemetry.user_queue_count = self.telemetry.user_queue_count.saturating_add(1);
        self.telemetry.record_depth(self.q.len());
        Ok(())
    }

    /// Queue one kernel-generated packet.
    ///
    /// Kernel packets can consume the reserved portion of the queue but still
    /// cannot exceed total capacity.
    pub fn queue_kernel(&mut self, pkt: Packet) -> Result<(), PortError> {
        debug_assert_ne!(pkt.kind, PacketKind::User);
        let _ticket = self.reserve_kernel_slot().ok_or_else(|| {
            self.telemetry.kernel_should_wait_count =
                self.telemetry.kernel_should_wait_count.saturating_add(1);
            PortError::ShouldWait
        })?;
        if self.q.push_back(pkt).is_err() {
            self.telemetry.kernel_should_wait_count =
                self.telemetry.kernel_should_wait_count.saturating_add(1);
            return Err(PortError::ShouldWait);
        }
        self.telemetry.kernel_queue_count = self.telemetry.kernel_queue_count.saturating_add(1);
        self.telemetry.record_depth(self.q.len());
        Ok(())
    }

    /// Pop one packet (non-blocking).
    pub fn pop(&mut self) -> Result<Packet, PortError> {
        let Some(pkt) = self.q.pop_front() else {
            return Err(PortError::ShouldWait);
        };
        if pkt.kind == PacketKind::User {
            debug_assert!(self.user_in_q > 0);
            self.user_in_q -= 1;
        }
        self.telemetry.pop_count = self.telemetry.pop_count.saturating_add(1);
        self.telemetry.record_depth(self.q.len());
        Ok(pkt)
    }

    /// Retain only kernel-generated packets that satisfy `keep`.
    ///
    /// User packets are left untouched.
    pub fn retain_kernel_packets<F>(&mut self, mut keep: F) -> usize
    where
        F: FnMut(&Packet) -> bool,
    {
        let before = self.q.len();
        self.q
            .retain(|packet| packet.kind == PacketKind::User || keep(packet));
        let removed = before.saturating_sub(self.q.len());
        if removed != 0 {
            self.telemetry.record_depth(self.q.len());
        }
        removed
    }

    /// Remove and return every kernel-generated packet that does not satisfy `keep`.
    ///
    /// User packets are preserved in-place and maintain their relative order.
    /// This is intended for maintenance paths such as revocation cleanup where
    /// the caller needs the removed packets' metadata rather than just a count.
    pub fn drain_kernel_packets_where<F>(&mut self, mut keep: F) -> Vec<Packet>
    where
        F: FnMut(&Packet) -> bool,
    {
        let initial_len = self.q.len();
        if initial_len == 0 {
            return Vec::new();
        }

        let mut kept = Vec::with_capacity(initial_len);
        let mut removed = Vec::new();
        for _ in 0..initial_len {
            let Some(packet) = self.q.pop_front() else {
                break;
            };
            if packet.kind == PacketKind::User || keep(&packet) {
                kept.push(packet);
            } else {
                removed.push(packet);
            }
        }

        self.user_in_q = 0;
        for packet in kept {
            if packet.kind == PacketKind::User {
                self.user_in_q = self.user_in_q.saturating_add(1);
            }
            debug_assert!(self.q.push_back(packet).is_ok());
        }

        if !removed.is_empty() {
            self.telemetry.record_depth(self.q.len());
        }
        removed
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn user_quota_enforces_kernel_reserve() {
        let mut port = Port::new(4, 2);
        port.queue_user(Packet::user(1)).unwrap();
        port.queue_user(Packet::user(2)).unwrap();
        assert_eq!(port.queue_user(Packet::user(3)), Err(PortError::ShouldWait));
        assert_eq!(port.len(), 2);
    }

    #[test]
    fn kernel_packet_can_use_reserved_slot() {
        let mut port = Port::new(4, 2);
        port.queue_user(Packet::user(1)).unwrap();
        port.queue_user(Packet::user(2)).unwrap();
        assert_eq!(port.len(), 2);
        port.queue_kernel(Packet::signal(
            7,
            42.into(),
            Signals::CHANNEL_READABLE,
            Signals::CHANNEL_READABLE,
            1,
            12,
        ))
        .unwrap();
        assert_eq!(port.len(), 3);
    }

    #[test]
    fn kernel_packet_still_obeys_total_capacity() {
        let mut port = Port::new(2, 1);
        port.queue_user(Packet::user(1)).unwrap();
        port.queue_kernel(Packet::signal(
            2,
            10.into(),
            Signals::TIMER_SIGNALED,
            Signals::TIMER_SIGNALED,
            1,
            99,
        ))
        .unwrap();
        assert_eq!(
            port.queue_kernel(Packet::signal(
                3,
                10.into(),
                Signals::TIMER_SIGNALED,
                Signals::TIMER_SIGNALED,
                1,
                100,
            )),
            Err(PortError::ShouldWait)
        );
    }

    #[test]
    fn retain_kernel_packets_drops_only_revoked_control_packets() {
        let mut mgr = crate::revocation::RevocationManager::new();
        let live_token = mgr.create_group();
        let revoked_token = mgr.create_group();
        let live_ref = mgr.snapshot(live_token).unwrap();
        let revoked_ref = mgr.snapshot(revoked_token).unwrap();
        mgr.revoke(revoked_token).unwrap();
        let current_epoch = mgr.epoch_of(revoked_token.id()).unwrap();

        let mut port = Port::new(8, 2);
        port.queue_user(Packet::user(11)).unwrap();
        port.queue_kernel(Packet::signal_with_revocation(
            1,
            10.into(),
            Signals::TIMER_SIGNALED,
            Signals::TIMER_SIGNALED,
            1,
            10,
            crate::RevocationSet::one(Some(live_ref)),
        ))
        .unwrap();
        port.queue_kernel(Packet::signal_with_revocation(
            2,
            20.into(),
            Signals::CHANNEL_READABLE,
            Signals::CHANNEL_READABLE,
            1,
            20,
            crate::RevocationSet::one(Some(revoked_ref)),
        ))
        .unwrap();

        let removed = port.retain_kernel_packets(|packet| {
            !packet.revocation.contains_revoked(
                revoked_token.id(),
                revoked_token.generation(),
                current_epoch,
            )
        });
        assert_eq!(removed, 1);

        let first = port.pop().unwrap();
        assert_eq!(first.kind, PacketKind::User);
        let second = port.pop().unwrap();
        assert_eq!(second.key, 1);
        assert!(port.is_empty());
    }
}
